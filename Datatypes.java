/*
 Varibale -It is a name of memory location  where we store data .
 Datatype:It show which type of data variable will store.

 1)int (integer)
 2)char(character)-It store single enclosed in single quotes.
 3)float(decimal values)-In java we have to use F infront of decimal values.
 4)String-It is a collection of characters enclosed in double quotes.
 5)Boolean-It store two values true and false.
 6)byte[]-It store every data in byte form.
 */
public class Datatypes {
public static void main(String[] args) {
    int age=22;
    String name="Sayali";
    float percent=89.78F;
    char grade='A';
    boolean check =true;


    System.out.println("Age="+age);
    System.out.println("Name="+name);
    System.out.println("Percent="+percent);
    System.out.println("grade="+grade);
    System.out.println("check="+check);
}
    
}